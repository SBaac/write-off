 <h2>CHECK LIST </h2>
 <hr class="hr_shadow">
 <br>
เรียงเอกสารที่ส่ง สนจ.(สำเนา) ตัวจริงเก็บที่สาขา ให้ฉีกปะหน้าเอกสารแต่ละราย(ชุด)		
<br>&#9634;	1) รายงานการประชุม คณะกรรมการจำหน่ายหนี้ออกจากบัญชีเป็นหนี้สูญระดับสาขา	
<br>&#9634;	2) แบบสรุปรายละเอียดขอจำหน่ายหนี้เงินกู้ออกจากบัญชีเป็นหนี้สูญ(Word) - Print	ใช้ลวดเย็บกระดาษ เป็นชุด
<br>&#9634;	3) ทะเบียนผู้กู้ (จาก CBS) 	
<br>&#9634;	4) Account Summary2 (จาก CBS)	
<br>&#9634;	5) ใบแจ้งยอดบัญชีสินเชื่อ(Loan Account Statement) (จาก CBS)  	
<br>&#9634;	6) รายการเคลื่อนไหวบัญชีลูกค้าเงินกู้(STATEMENT)ในระบบเดิม สัญญาก่อนปปน.และปปน. 	
<br>&#9634;	7) สัญญาเงินกู้เดิม  สัญญาเงินกู้ ปปน. ข้อตกลงต่อท้ายพักชำระหนี้	
<br>&#9634;	8) สัญญารับใช้หนี้(ทำให้อายุความสะดุด) ถ้ามี	
<br>&#9634;	9) สำเนาทะเบียนบ้าน (กรณีตาย หรือย้ายที่อยู่)	
<br>&#9634;	10) ข้อมูลลูกค้ารายคน	
<br>&#9634;	11) ไฟล์ Word,Power Point และbak  รวมไฟล์ทุกรายไว้ในโฟลเดอร์เดียวกันแล้ว ZIP โฟลเดอร์แล้วส่งเมลล์ให้สนจ.	
